public class Variables {
    public static void main(String[] args) {

        int hour = 3, minute = 5;

        System.out.println(hour + "h" + minute + "m");
        System.out.println(hour * 60 + minute + "m");

        int totalMinute = hour * 60 + minute;

        System.out.println(totalMinute);
    }
}
